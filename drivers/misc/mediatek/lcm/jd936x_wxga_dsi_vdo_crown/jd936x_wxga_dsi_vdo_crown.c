static void lcm_init_lcm(void)
{
	get_lcm_id();

	 if (vendor_id == ST_INX)
		init_crown_jd936x_inx_lcm(); 		/* JD936X INX panel */
	else if (vendor_id == ST_KD_INX)
		init_crown_jd936x_kd_inx_lcm();		/* JD936X KD panel, INX glass */
	else if (vendor_id == ST_KD_HSD)
		init_crown_jd936x_kd_hsd_lcm();		/* JD936X KD panel, HSD glass */
	else if (vendor_id == ST_BOE)
		init_crown_jd936x_boe_lcm();		/* JD936X BOE panel */
	else if (vendor_id == ST_PROTO_INX)
		init_crown_jd936x_proto_inx_lcm();	/* JD936X PROTO_INX panel */
	else if (vendor_id == ST_PROTO_KD_INX)
		init_crown_jd936x_proto_kd_inx_lcm();	/* JD936X PROTO_KD panel, INX glass */
	else if (vendor_id == ST_PROTO_KD_HSD)
		init_crown_jd936x_proto_kd_hsd_lcm();	/* JD936X PROTO_KD panel, HSD glass */
	else if (vendor_id == ST_ILITECK_HSD)
		init_crown_iliteck_hsd_lcm();		/* ILITECK HSD panel */
	else if (vendor_id == ST_ILITECK_BOE)
		init_crown_iliteck_boe_lcm();		/* ILITECK BOE panel*/
	else
		init_crown_jd936x_inx_lcm();			/* Default JD936X INX panel */

	pr_debug("[JD936X_lcm]lcm_init_lcm func:Kernel Crown jd936x %s lcm init ok!\n",lcm_get_vendor_type());
}

static void lcm_suspend(void)
{
	if(vendor_id != ST_ILITECK_BOE && vendor_id!= ST_ILITECK_HSD) {
		push_table(lcm_suspend_setting_jd936x, sizeof(lcm_suspend_setting_jd936x) / sizeof(struct LCM_setting_table), 1);
	}
	else {
		push_table(lcm_suspend_setting_iliteck, sizeof(lcm_suspend_setting_iliteck) / sizeof(struct LCM_setting_table), 1);
	}
}

static void lcm_suspend_power(void)
{
	pr_debug("LCM: lcm try to turn off supply\n");

	MDELAY(50);
	lcm_set_gpio_output(GPIO_LCD_RST_EN,GPIO_OUT_LOW);
	MDELAY(120);
	lcm_set_gpio_output(GPIO_LCD_PWR_N_EN,GPIO_OUT_LOW);
	MDELAY(10);
	lcm_set_gpio_output(GPIO_LCD_PWR_P_EN,GPIO_OUT_LOW);
	MDELAY(10);

	if (lcm_vio_supply_disable())
		pr_err(KERN_ERR "<%s>JD936X lcm(lcm_vio) failed to turn off supply", __func__);
}

static void lcm_resume_power(void)
{
	pr_debug("LCM: lcm try to turn on supply\n");

	if(lcm_vio_supply_enable())
		pr_err(KERN_ERR "<%s>JD936X lcm(lcm_vio) failed to turn on supply", __func__);

	lcm_set_gpio_output(GPIO_LCD_PWR_P_EN,GPIO_OUT_HIGH);
	MDELAY(10);
	lcm_set_gpio_output(GPIO_LCD_PWR_N_EN,GPIO_OUT_HIGH);

	lcm_i2c_set_data(LCM_STATUS_OK, &lcm_i2c_data);

}

static void lcm_resume(void)
{
	unsigned char buf[4];
	get_lcm_id();

	MDELAY(10);
	lcm_reset();

	if (vendor_id == ST_INX)
		init_crown_jd936x_inx_lcm();		/* JD936X INX panel */
	else if (vendor_id == ST_KD_INX)
		init_crown_jd936x_kd_inx_lcm();		/* JD936X KD panel, INX glass */
	else if (vendor_id == ST_KD_HSD)
		init_crown_jd936x_kd_hsd_lcm();		/* JD936X KD panel, HSD glass */
	else if (vendor_id == ST_BOE)
		init_crown_jd936x_boe_lcm();		/* JD936X BOE panel */
	else if (vendor_id == ST_PROTO_INX)
		init_crown_jd936x_proto_inx_lcm();	/* JD936X PROTO_INX panel */
	else if (vendor_id == ST_PROTO_KD_INX)
		init_crown_jd936x_proto_kd_inx_lcm();	/* JD936X PROTO_KD panel, INX glass */
	else if (vendor_id == ST_PROTO_KD_HSD)
		init_crown_jd936x_proto_kd_hsd_lcm();	/* JD936X PROTOINX panel, HSD glass */
	else if (vendor_id == ST_ILITECK_HSD)
		init_crown_iliteck_hsd_lcm();		/* ILITECK HSD panel */
	else if (vendor_id == ST_ILITECK_BOE)
		init_crown_iliteck_boe_lcm();		/* ILITECK BOE panel*/
	else
		init_crown_jd936x_inx_lcm();		/* Default JD936X INX panel */

	read_reg_v2(0x0A, &buf[0], 1);	/* ESD: read 1 bytes from 0Ah */
	pr_debug("[KER/LCM] %s, ESD:0x%x\n", __func__, buf[0]);
}

