/*
 * Minimal routing table with one route implemented:
 * OVL0 -> COLOR0 -> CCORR -> AAL -> GAMMA -> DITHER -> RDMA0 -> UFOE -> DSI0
 *
 * MT8163 has other routes for driving external displays that are not
 * implemented here.
 */
static const struct mtk_mmsys_routes mt8163_mmsys_routing_table[] = {
	MMSYS_ROUTE(OVL0, COLOR0,
		    MT8163_DISP_REG_CONFIG_DISP_OVL0_MOUT_EN, MT8163_OVL0_MOUT_EN_MASK,
		    OVL0_MOUT_EN_COLOR0),
	MMSYS_ROUTE(OVL0, COLOR0,
		    MT8163_DISP_REG_CONFIG_DISP_COLOR0_SEL_IN, COLOR0_SEL_IN_OVL0,
		    COLOR0_SEL_IN_OVL0),

	MMSYS_ROUTE(DITHER0, RDMA0,
		    MT8163_DISP_REG_CONFIG_DISP_DITHER_MOUT_EN, MT8163_DITHER_MOUT_EN_MASK,
		    MT8163_DITHER_MOUT_EN_RDMA0),

	MMSYS_ROUTE(RDMA0, UFOE,
		    MT8163_DISP_REG_CONFIG_DISP_RDMA0_SOUT_SEL_IN, MT8163_RDMA0_SOUT_MASK,
		    0 /* Single output to DISP_UFOE */),
	MMSYS_ROUTE(RDMA0, UFOE,
		    MT8163_DISP_REG_CONFIG_DISP_UFOE_SEL_IN, MT8163_UFOE_SEL_IN_MASK,
		    0 /* Input from DISP_RDMA0 */),

	MMSYS_ROUTE(UFOE, DSI0,
		    MT8163_DISP_REG_CONFIG_DISP_UFOE_MOUT_EN, MT8163_UFOE_MOUT_EN_MASK,
		    UFOE_MOUT_EN_DSI0),
	MMSYS_ROUTE(UFOE, DSI0,
		    MT8163_DISP_REG_CONFIG_DISP_DSI0_SEL_IN, MT8163_DSI0_SEL_IN_MASK,
		    0 /* Input from DISP_UFOE */),
};

