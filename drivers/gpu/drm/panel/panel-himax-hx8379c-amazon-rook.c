static const struct rook_hx8379c_info*
rook_hx8379c_get_panel_info(struct rook_hx8379c *ctx)
{
	int id0, id1, id;

	id0 = gpiod_get_value_cansleep(ctx->id0_gpio);
	id1 = gpiod_get_value_cansleep(ctx->id1_gpio);

	id = (id1 << 1) | id0;

	pr_err("%s: Panel ID: %d", __func__, id0);

	switch (id) {
		case PANEL_INX:
		case PANEL_INXE:
			return &rook_hx8379c_inx_info;
		case PANEL_TIANMA:
			return &rook_hx8379c_tianma_info;
		case PANEL_BOE:
			return &rook_hx8379c_boe_info;
		default:
			return ERR_PTR(-EINVAL);
	}
}

